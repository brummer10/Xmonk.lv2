// constructor
Xmonk_::Xmonk_() :
  note(NULL),
  gate(NULL),
  panic(NULL),
  vowel(NULL),
  output(NULL),
  output1(NULL),
  xmonk(xmonk::plugin()),
  reverb(stereoverb::plugin()),
  delay(stereodelay::plugin()) {};

// destructor
Xmonk_::~Xmonk_()
{
  // delete DSP class
  xmonk->del_instance(xmonk);
  reverb->del_instance(reverb);
  delay->del_instance(delay);
};

